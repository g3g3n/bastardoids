import type { ShipModelDefinition, ShipModelName, VectorTuple } from "../types";

type Segment = readonly [VectorTuple, VectorTuple];

const mirrorX = ([x, y, z]: VectorTuple): VectorTuple => [-x, y, z];
const mirrorSegmentsX = (segments: readonly Segment[]): Segment[] =>
  segments.map(([from, to]) => [mirrorX(from), mirrorX(to)] as const);

const loopSegments = (points: readonly VectorTuple[]): Segment[] => {
  const segments: Segment[] = [];
  for (let index = 0; index < points.length; index += 1) {
    segments.push([points[index], points[(index + 1) % points.length]]);
  }
  return segments;
};

